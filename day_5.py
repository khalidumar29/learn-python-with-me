# This script covers if-else conditional statements in Python.

# ==========================
# Part 1: if Statement
# ==========================
# The if statement is used to check a condition.
# If the condition is true, the code block inside the if statement is executed.

# Example of if statement
x = 10
if x > 5:
    print("x is greater than 5")

# ==========================
# Part 2: if-else Statement
# ==========================
# The if-else statement allows you to execute one block of code.
# if the condition is true and another block if the condition is false.

# Example of if-else statement
y = 3
if y % 2 == 0:
    print("y is even")
else:
    print("y is odd")

# ==========================
# Part 3: elif Statement
# ==========================
# The elif statement is used to check multiple conditions.
# It comes after an if statement and before an optional else statement.

# Example of if-elif-else statement
z = 0
if z > 0:
    print("z is positive")
elif z < 0:
    print("z is negative")
else:
    print("z is zero")

# ==========================
# Summary
# ==========================
# In this script, we covered:
# 1. The if statement for checking a condition.
# 2. The if-else statement for executing different blocks of code based on a condition.
# 3. The elif statement for checking multiple conditions in sequence.

